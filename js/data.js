// ===== CRUD HELPERS =====
function getUsers() {
  try { const u = JSON.parse(localStorage.getItem(DB.USERS)); return u && u.length ? u : JSON.parse(JSON.stringify(DEFAULT_USERS)); }
  catch (e) { return JSON.parse(JSON.stringify(DEFAULT_USERS)); }
}
function saveUsers(u) { localStorage.setItem(DB.USERS, JSON.stringify(u)); }

function getDocs() {
  try { const d = JSON.parse(localStorage.getItem(DB.DOCS)); return d && d.length ? d : JSON.parse(JSON.stringify(DEFAULT_DOCS)); }
  catch (e) { return JSON.parse(JSON.stringify(DEFAULT_DOCS)); }
}
function saveDocs(d) { localStorage.setItem(DB.DOCS, JSON.stringify(d)); }

function getSession() {
  try { return JSON.parse(localStorage.getItem(DB.SESSION)) || null; }
  catch (e) { return null; }
}
function setSession(s) { localStorage.setItem(DB.SESSION, JSON.stringify(s)); }
function clearSession()  { localStorage.removeItem(DB.SESSION); }

function getUsage() {
  try {
    const u = JSON.parse(localStorage.getItem(DB.USAGE)) || {};
    const today = new Date().toDateString();
    if (u.date !== today) { const f = { date: today, words: 0 }; saveUsage(f); return f; }
    return u;
  } catch (e) { return { date: new Date().toDateString(), words: 0 }; }
}
function saveUsage(u) { localStorage.setItem(DB.USAGE, JSON.stringify(u)); }

// ===== QUOTA =====
function getLimit(session) {
  if (!session) return 500;
  return (session.plan === 'premium' || session.plan === 'admin') ? 999999 : 500;
}
